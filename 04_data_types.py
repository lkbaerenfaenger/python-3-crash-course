# int
age = 31

print(age)
print(type(age))

# float
pi = 3.14

print(pi)
print(type(pi))

# str
name = "Lucas"

print(name)
print(type(name))

# list
groceries = ["Milk", "Eggs", "Ice cream"]

print(groceries)
print(type(groceries))

# dict
person = {
    "name": "Lucas",
    "lang": "Python"
}

print(person)
print(person["name"])
print(type(person))

# tuple (are immutable)
kids = ("Nathan", "Zephyr")

print(kids)
print(type(kids))

# set
foods = {"Pizza", "Tacos", "Ice Cream", "Pizza", "Pizza", "Tacos"}

print(foods)
print(type(foods))

# bool
is_adult = True

print(is_adult)
print(type(is_adult))
